const month =3

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("Feb");
        break;    
    case 3:
        console.log("March");
        break;
    default:
        console.log("Default Value");
        break;
}

//Here must write "break",if you don't write "break" then all the case {except default case} 
//will be executed. Remember "default case" will not be executed if you don't write "break".